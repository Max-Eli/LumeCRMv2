# ── Public fill flow serializers (token-based, unauthenticated) ─────


class PublicFormSubmissionSerializer(serializers.ModelSerializer):
    """Read shape for the public fill page (`GET /api/forms/sign/<token>/`).

    Returns the schema + status + (when completed) the answers — the
    same surface serves "client filling out the form" (status pending)
    and "operator viewing the signed copy via the same URL" (status
    completed). Customer name is the only customer-PHI included so
    the page can show "Hi {name}" on the fill flow.

    Tenant + customer details beyond the name are deliberately
    excluded — this endpoint is unauthenticated; minimum information
    needed for the fill flow.
    """

    template_name = serializers.CharField(source='form_template.name', read_only=True)
    customer_first_name = serializers.CharField(source='customer.first_name', read_only=True)

    class Meta:
        model = FormSubmission
        fields = [
            'token',
            'template_name',
            'template_version_at_assignment',
            'schema_snapshot',
            'customer_first_name',
            'status',
            'answers',
            'signed_at',
        ]
        read_only_fields = fields


class PublicFormSignSerializer(serializers.Serializer):
    """Input for `POST /api/forms/sign/<token>/` — the client submits
    answers + signature canvas data.

    `answers` keyed by field id from the schema snapshot. Validation
    against the schema (required fields populated, choice values
    matching options, etc.) happens in the view because it depends
    on the snapshot loaded from the submission.

    `signature_data` is a base64-encoded PNG (data URL prefix accepted
    + stripped). Required — there's no consent flow without a
    signature.
    """

    answers = serializers.JSONField()
    signature_data = serializers.CharField(min_length=1, max_length=2_000_000)

    def validate_answers(self, value):
        if not isinstance(value, dict):
            raise serializers.ValidationError(
                'Answers must be a JSON object keyed by field id.',
            )
        return value
